#define SCREEN_SIZE        (SCROLL_SIZE * 4 + 1)
#define BUILD_BUF_SIZE     (SCREEN_SIZE + 20000)
#define BUILD_BASE_INIT    ((BUILD_BUF_SIZE - SCREEN_SIZE) / 2)

